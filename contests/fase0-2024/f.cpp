const int MOD = 998244353;
const int MAX = 5010;
int catalan[MAX];
int N;
string S;

void init() {
    catalan[0] = catalan[1] = 1;
    for (int i=2; i<=N; i++) {
        catalan[i] = 0;
        for (int j=0; j < i; j++) {
            catalan[i] += (catalan[j] * catalan[i-j-1]) % MOD;
            if (catalan[i] >= MOD) {
                catalan[i] -= MOD;
            }
        }
    }
}

